package com.mcf.pojoclasses;

public  class Addresses {
	private String abodeNumber;
	private int buildingNumber;
	private String buildingName;
	private String street;
	private String city;
	private  String post_code;
	private String from;
	private String to;
	private String residential_status;
	
	public String getAbodeNumber() {
		return abodeNumber;
	}
	public void setAbodeNumber(String abodeNumber) {
		this.abodeNumber = abodeNumber;
	}
	public int getBuildingNumber() {
		return buildingNumber;
	}
	public void setBuildingNumber(int buildingNumber) {
		this.buildingNumber = buildingNumber;
	}
	public String getBuildingName() {
		return buildingName;
	}
	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPost_code() {
		return post_code;
	}
	public void setPost_code(String post_code) {
		this.post_code = post_code;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getResidential_status() {
		return residential_status;
	}
	public void setResidential_status(String residential_status) {
		this.residential_status = residential_status;
	}
	

}

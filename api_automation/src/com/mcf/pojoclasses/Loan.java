package com.mcf.pojoclasses;

public class Loan {
private String amount;
private String currency;
private String termInMonths;
private String rentOrMortgage;
private String income;
private String employer;
private String purpose;
private String purpose_if_other;
private String outgoings;
private String employmentStatus;

public String getAmount() {
	return amount;
}
public void setAmount(String amount) {
	this.amount = amount;
}
public String getCurrency() {
	return currency;
}
public void setCurrency(String currency) {
	this.currency = currency;
}
public String getTermInMonths() {
	return termInMonths;
}
public void setTermInMonths(String termInMonths) {
	this.termInMonths = termInMonths;
}
public String getRentOrMortgage() {
	return rentOrMortgage;
}
public void setRentOrMortgage(String rentOrMortgage) {
	this.rentOrMortgage = rentOrMortgage;
}
public String getIncome() {
	return income;
}
public void setIncome(String income) {
	this.income = income;
}
public String getEmployer() {
	return employer;
}
public void setEmployer(String employer) {
	this.employer = employer;
}
public String getPurpose() {
	return purpose;
}
public void setPurpose(String purpose) {
	this.purpose = purpose;
}
public String getPurpose_if_other() {
	return purpose_if_other;
}
public void setPurpose_if_other(String purpose_if_other) {
	this.purpose_if_other = purpose_if_other;
}
public String getOutgoings() {
	return outgoings;
}
public void setOutgoings(String outgoings) {
	this.outgoings = outgoings;
}
public String getEmploymentStatus() {
	return employmentStatus;
}
public void setEmploymentStatus(String employmentStatus) {
	this.employmentStatus = employmentStatus;
}

}
